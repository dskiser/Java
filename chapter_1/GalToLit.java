class GalToLit {
	public static void main(String args[]) {

		double gallons;
		double litres;

		gallons = 10;
		litres = gallons * 3.7854;

		System.out.println("Number of gallons: " + gallons);
		System.out.println("Number of litres: " + litres);
	}
}
